class Assignment_operator{
    public static void main(String args[]){
        int no=10;
        no+=5;  //no=no+5;
        System.out.println("no+=5:"+no);
        no-=5;   //no=no-5;
        System.out.println("no-=5:"+no);
        no*=5;   //no=no*5;
        System.out.println("no*=5:"+no);
        no/=5;    //no=no/5;
        System.out.println("no/=5:"+no);


        
    
    }}